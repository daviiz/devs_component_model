		ViewableAtomic proc = new displaydataproc("DataProc");
		CellGridPlot t = new CellGridPlot("Moving Plot", 0, 1200, 1200);
		t.setCellGridViewLocation(600, 300);

		add(proc);
		add(t);

		addCoupling(this, "p1in", proc, "p1in");
		addCoupling(this, "p2in", proc, "p2in");
		addCoupling(this, "p3in", proc, "p3in");
