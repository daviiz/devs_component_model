		addCoupling(this, "endx", proc, "o3");
		addCoupling(this, "endy", proc, "o4");
		addCoupling(proc, "out", t, "drawCellToScale");

		initialize();
		preferredSize = new Dimension(100, 50);
		proc.setPreferredLocation(new Point(13, 18));

